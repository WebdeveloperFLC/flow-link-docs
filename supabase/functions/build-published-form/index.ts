import { createClient } from "https://esm.sh/@supabase/supabase-js@2.45.0";
import { PDFDocument, StandardFonts, rgb, PDFFont, PDFPage } from "npm:pdf-lib@1.17.1";

const cors = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};
const json = (b: unknown, s = 200) =>
  new Response(JSON.stringify(b), { status: s, headers: { ...cors, "Content-Type": "application/json" } });

type FieldDef = {
  id: string;
  pdf_field?: string;
  label: string;
  type: "text" | "textarea" | "date" | "number" | "dropdown" | "yes_no" | "multi_entry";
  options?: string[];
  required?: boolean;
};
type SectionDef = { key: string; label: string; fields: FieldDef[] };

/** Sanitize a string into a stable AcroForm field name. */
function sanitizeFieldName(raw: string, fallback: string): string {
  const s = (raw || fallback).replace(/[^a-zA-Z0-9_]/g, "_").replace(/_+/g, "_").replace(/^_|_$/g, "");
  return s || fallback;
}

function wrap(text: string, font: PDFFont, size: number, maxWidth: number): string[] {
  if (!text) return [""];
  const words = sanitizeText(text).split(/\s+/);
  const lines: string[] = [];
  let line = "";
  for (const w of words) {
    const trial = line ? `${line} ${w}` : w;
    if (font.widthOfTextAtSize(trial, size) <= maxWidth) line = trial;
    else { if (line) lines.push(line); line = w; }
  }
  if (line) lines.push(line);
  return lines;
}

/** Strip characters that StandardFonts (WinAnsi) cannot encode — these crash pdf-lib. */
function sanitizeText(s: string): string {
  return (s ?? "")
    .replace(/[\u2018\u2019\u02BC]/g, "'")
    .replace(/[\u201C\u201D]/g, '"')
    .replace(/[\u2013\u2014]/g, "-")
    .replace(/\u2022/g, "*")
    .replace(/\u00A0/g, " ")
    .replace(/[^\x20-\x7E]/g, "");
}

async function buildPdf(args: {
  formName: string;
  formCode?: string;
  version: number;
  sections: SectionDef[];
}): Promise<Uint8Array> {
  const pdf = await PDFDocument.create();
  const helv = await pdf.embedFont(StandardFonts.Helvetica);
  const helvBold = await pdf.embedFont(StandardFonts.HelveticaBold);
  const form = pdf.getForm();

  const PAGE_W = 612, PAGE_H = 792;
  const MX = 48, MTOP = 56, MBOT = 56;
  const LABEL_SIZE = 9;
  const FIELD_H = 18;
  const ROW_GAP = 10;
  const SECTION_GAP = 16;
  const PRIMARY = rgb(0.10, 0.20, 0.45);
  const MUTED = rgb(0.40, 0.40, 0.45);
  const RULE = rgb(0.85, 0.87, 0.92);

  let pageNum = 0;
  let page: PDFPage = pdf.addPage([PAGE_W, PAGE_H]);
  pageNum++;
  let y = PAGE_H - MTOP;

  const drawHeader = (p: PDFPage, n: number) => {
    p.drawText(args.formName, { x: MX, y: PAGE_H - 36, size: 14, font: helvBold, color: PRIMARY });
    const sub = `${args.formCode ? args.formCode + "  ·  " : ""}v${args.version}  ·  Generated by Lovable CRM`;
    p.drawText(sub, { x: MX, y: PAGE_H - 52, size: 8.5, font: helv, color: MUTED });
    p.drawLine({
      start: { x: MX, y: PAGE_H - 60 }, end: { x: PAGE_W - MX, y: PAGE_H - 60 },
      thickness: 0.6, color: RULE,
    });
    p.drawText(`Page ${n}`, { x: PAGE_W - MX - 40, y: 32, size: 8, font: helv, color: MUTED });
  };
  drawHeader(page, pageNum);
  y = PAGE_H - 80;

  const newPage = () => {
    page = pdf.addPage([PAGE_W, PAGE_H]); pageNum++;
    drawHeader(page, pageNum); y = PAGE_H - 80;
  };
  const ensure = (h: number) => { if (y - h < MBOT) newPage(); };

  // Track used names to avoid AcroForm collisions (must be unique).
  const usedNames = new Set<string>();
  const uniqueName = (base: string): string => {
    let n = base; let i = 2;
    while (usedNames.has(n)) { n = `${base}_${i++}`; }
    usedNames.add(n);
    return n;
  };

  for (const sec of args.sections) {
    if (!sec.fields?.length) continue;
    ensure(40);
    page.drawRectangle({
      x: MX, y: y - 4, width: PAGE_W - MX * 2, height: 20,
      color: rgb(0.94, 0.96, 1),
    });
    page.drawText(sec.label.toUpperCase(), {
      x: MX + 8, y: y + 2, size: 10, font: helvBold, color: PRIMARY,
    });
    y -= 24;

    // Two-column grid for short fields, full row for textarea / dropdown w/ many options.
    const colWidth = (PAGE_W - MX * 2 - 12) / 2;
    let col: 0 | 1 = 0;
    let rowTop = y;

    const placeField = (f: FieldDef, fullRow: boolean) => {
      const widthBox = fullRow ? PAGE_W - MX * 2 : colWidth;
      const xBox = MX + (col === 1 ? colWidth + 12 : 0);
      const labelLines = wrap(f.label, helv, LABEL_SIZE, widthBox);
      const labelH = labelLines.length * (LABEL_SIZE + 2);
      const fieldH = f.type === "textarea" ? FIELD_H * 3 : FIELD_H;
      const blockH = labelH + 4 + fieldH;
      ensure(blockH + ROW_GAP);

      // Recompute after possible newPage
      if (col === 0) rowTop = y;
      const top = rowTop;

      labelLines.forEach((ln, i) => {
        page.drawText(ln, {
          x: xBox, y: top - (i + 1) * (LABEL_SIZE + 2) + 2,
          size: LABEL_SIZE, font: helvBold, color: rgb(0.20, 0.22, 0.30),
        });
      });
      if (f.required) {
        const lw = helvBold.widthOfTextAtSize(labelLines[0] || "", LABEL_SIZE);
        page.drawText(" *", {
          x: xBox + lw, y: top - (LABEL_SIZE + 2) + 2,
          size: LABEL_SIZE, font: helvBold, color: rgb(0.85, 0.20, 0.20),
        });
      }
      const fieldY = top - labelH - 4 - fieldH;
      const fieldName = uniqueName(sanitizeFieldName(f.pdf_field ?? f.id, "field"));

      try {
        if (f.type === "yes_no") {
          const cb = form.createCheckBox(fieldName);
          cb.addToPage(page, { x: xBox, y: fieldY + 2, width: 14, height: 14 });
          page.drawText("Yes / Check if applicable", {
            x: xBox + 20, y: fieldY + 5, size: 9, font: helv, color: MUTED,
          });
        } else if (f.type === "dropdown" && f.options?.length) {
          const dd = form.createDropdown(fieldName);
          dd.setOptions(f.options.map((o) => sanitizeText(String(o))));
          dd.addToPage(page, { x: xBox, y: fieldY, width: widthBox, height: fieldH, font: helv });
        } else {
          const tx = form.createTextField(fieldName);
          if (f.type === "textarea") tx.enableMultiline();
          tx.addToPage(page, { x: xBox, y: fieldY, width: widthBox, height: fieldH, font: helv });
        }
      } catch (e) {
        // If a single field fails to render, draw a placeholder box and continue
        console.warn("placeField failed", fieldName, String(e));
        page.drawRectangle({
          x: xBox, y: fieldY, width: widthBox, height: fieldH,
          borderColor: RULE, borderWidth: 0.5,
        });
      }

      if (fullRow) {
        col = 0;
        y = fieldY - ROW_GAP;
        rowTop = y;
      } else {
        if (col === 0) {
          col = 1;
        } else {
          col = 0;
          y = fieldY - ROW_GAP;
          rowTop = y;
        }
      }
    };

    for (const f of sec.fields) {
      const fullRow =
        f.type === "textarea" ||
        f.type === "multi_entry" ||
        (f.type === "dropdown" && (f.options?.length ?? 0) > 6) ||
        f.label.length > 70;
      // If we're in col=1 and need a full row, close the row first.
      if (fullRow && col === 1) {
        col = 0;
        y = rowTop - 60; // drop a row
      }
      placeField(f, fullRow);
    }
    // Close any open half-row.
    if (col === 1) { col = 0; y = rowTop - 60; }
    y -= SECTION_GAP;
  }

  // Footer note
  ensure(30);
  y = Math.max(y, MBOT + 20);
  page.drawText("This is the published, fillable PDF generated from the visa form schema.", {
    x: MX, y: 20, size: 7.5, font: helv, color: MUTED,
  });

  return await pdf.save();
}

Deno.serve(async (req) => {
  if (req.method === "OPTIONS") return new Response("ok", { headers: cors });
  try {
    const SUPABASE_URL = Deno.env.get("SUPABASE_URL")!;
    const SERVICE_KEY = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!;
    const auth = req.headers.get("Authorization") ?? "";
    const supabase = createClient(SUPABASE_URL, SERVICE_KEY, { global: { headers: { Authorization: auth } } });
    const token = auth.replace("Bearer ", "");
    const { data: userResp } = await supabase.auth.getUser(token);
    if (!userResp.user) return json({ error: "Unauthorized" }, 401);

    const body = await req.json().catch(() => ({}));
    const form_id = String(body.form_id ?? "");
    const sections = (body.sections ?? []) as SectionDef[];
    const country = body.country ? String(body.country) : undefined;
    const category = body.category ? String(body.category) : undefined;
    const email_template_id = body.email_template_id ? String(body.email_template_id) : null;
    const auto_questionnaire = Boolean(body.auto_questionnaire ?? true);
    const requires_validation = Boolean(body.requires_validation ?? false);
    if (!form_id) return json({ error: "form_id required" }, 400);
    if (!Array.isArray(sections) || sections.length === 0) return json({ error: "sections required" }, 400);

    const { data: form, error: formErr } = await supabase
      .from("visa_forms").select("*").eq("id", form_id).maybeSingle();
    if (formErr || !form) return json({ error: "Form not found" }, 404);

    // 1) Build the published AcroForm PDF
    const pdfBytes = await buildPdf({
      formName: form.name,
      formCode: form.code ?? undefined,
      version: (form.version ?? 1) + 1,
      sections,
    });

    const stamp = new Date().toISOString().replace(/[:.]/g, "-");
    const publishedPath = `${form.country}/${form.category}/${form.id}-published-${stamp}.pdf`;
    const { error: upErr } = await supabase.storage.from("visa-forms")
      .upload(publishedPath, pdfBytes, { contentType: "application/pdf", upsert: true });
    if (upErr) return json({ error: `Upload failed: ${upErr.message}` }, 500);

    // 2) Save schema as active (deactivate previous active for same form)
    await supabase.from("questionnaire_schemas")
      .update({ is_active: false })
      .eq("form_id", form_id).eq("is_active", true);

    const { data: prev } = await supabase.from("questionnaire_schemas")
      .select("version").eq("form_id", form_id).order("version", { ascending: false }).limit(1).maybeSingle();
    const nextVersion = (prev?.version ?? 0) + 1;

    const mappings: Record<string, { table: string; column: string }> = {};
    const PROFILE_KEYS = new Set([
      "date_of_birth","gender","nationality","place_of_birth",
      "passport_number","passport_issue_date","passport_expiry","passport_country",
      "marital_status","spouse_name",
      "address_line1","address_city","address_state","address_country","address_postal",
      "phone_alt","email_alt",
      "highest_qualification","institution_name","graduation_year","gpa_or_percentage",
      "employer_name","job_title","annual_income","currency",
      "bank_name","account_balance",
    ]);
    for (const s of sections) for (const f of s.fields) {
      const key = (f as { mapping_key?: string }).mapping_key;
      if (key && PROFILE_KEYS.has(key)) mappings[f.id] = { table: "client_profile", column: key };
    }

    const { data: schema, error: schErr } = await supabase.from("questionnaire_schemas").insert({
      form_id,
      name: `${form.name} questionnaire`,
      country: form.country,
      category: form.category,
      version: nextVersion,
      sections,
      mappings,
      is_active: true,
      is_draft: false,
      generated_by_ai: false,
      created_by: userResp.user.id,
    }).select("id, version").single();
    if (schErr) return json({ error: `Schema save failed: ${schErr.message}` }, 500);

    // 3) Update visa_forms with published references and settings
    const updates: Record<string, unknown> = {
      published_pdf_path: publishedPath,
      published_schema_id: schema.id,
      version: nextVersion,
      is_active: true,
      auto_questionnaire,
      requires_validation,
      builder_state: {},
    };
    if (country) updates.country = country;
    if (category) updates.category = category;
    if (email_template_id !== null) updates.email_template_id = email_template_id;
    if (!form.source_pdf_path) updates.source_pdf_path = form.file_path;
    await supabase.from("visa_forms").update(updates).eq("id", form_id);

    await supabase.from("activity_logs").insert({
      action: "form.published",
      entity_type: "visa_form",
      entity_id: form_id,
      user_id: userResp.user.id,
      details: { schema_id: schema.id, version: nextVersion, published_pdf_path: publishedPath },
    });

    return json({
      ok: true,
      published_pdf_path: publishedPath,
      schema_id: schema.id,
      version: nextVersion,
    });
  } catch (e) {
    console.error("build-published-form error", e);
    return json({ error: e instanceof Error ? e.message : "Failed" }, 500);
  }
});