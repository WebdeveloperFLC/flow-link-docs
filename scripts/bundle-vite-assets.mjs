#!/usr/bin/env node
/** Pre-bundle static assets into TS so Vite never readFile()s them at build time (Lovable-safe). */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "node:url";

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");

function bundleDir({ srcDir, outFile, exportName, ext = ".md" }) {
  const absDir = path.join(ROOT, srcDir);
  if (!fs.existsSync(absDir)) {
    console.warn(`[bundle-vite-assets] Skip missing dir: ${srcDir}`);
    return 0;
  }
  const files = fs.readdirSync(absDir).filter((f) => f.endsWith(ext)).sort();
  const entries = files.map((file) => {
    const content = fs.readFileSync(path.join(absDir, file), "utf8");
    return `  ${JSON.stringify(file)}: ${JSON.stringify(content)}`;
  });
  const ts = `// Auto-generated by scripts/bundle-vite-assets.mjs — do not edit.
export const ${exportName}: Record<string, string> = {
${entries.join(",\n")}
};
`;
  fs.mkdirSync(path.dirname(path.join(ROOT, outFile)), { recursive: true });
  fs.writeFileSync(path.join(ROOT, outFile), ts);
  return files.length;
}

function bundleMbbsJson() {
  const dataDir = path.join(ROOT, "src/lib/service-library/feeBreakdown/data");
  const fallbackDir = path.join(ROOT, "content/service-library");
  const srcDir = fs.existsSync(dataDir) ? dataDir : fallbackDir;
  if (!fs.existsSync(srcDir)) {
    console.warn("[bundle-vite-assets] Skip MBBS JSON — no data dir");
    return 0;
  }

  const files = fs
    .readdirSync(srcDir)
    .filter((f) => f.startsWith("mbbs-") && f.endsWith(".json"))
    .sort();
  const exports = files.map((file) => {
    const base = file.replace(/\.json$/, "");
    const exportName = base
      .replace(/^mbbs-/, "")
      .replace(/-/g, "_")
      .replace(/^([^_])/, (_, c) => c.toUpperCase());
    const constName =
      base === "mbbs-tuition-data"
        ? "tuitionData"
        : base.replace(/^mbbs-/, "").replace(/-/g, "_");
    const json = JSON.parse(fs.readFileSync(path.join(srcDir, file), "utf8"));
    return `export const ${constName} = ${JSON.stringify(json)} as const;`;
  });

  const outFile = path.join(ROOT, "src/lib/service-library/feeBreakdown/data/mbbsData.generated.ts");
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(
    outFile,
    `// Auto-generated by scripts/bundle-vite-assets.mjs — do not edit.\n${exports.join("\n\n")}\n`,
  );
  return files.length;
}

const guideCount = bundleDir({
  srcDir: "src/guides/content",
  outFile: "src/guides/lib/guideContent.generated.ts",
  exportName: "GUIDE_CONTENT_BY_FILE",
});
const aiCount = bundleDir({
  srcDir: "src/ai-help/knowledge",
  outFile: "src/ai-help/lib/crmKnowledge.generated.ts",
  exportName: "AI_HELP_KNOWLEDGE_BY_FILE",
});
const mbbsCount = bundleMbbsJson();

console.log(
  `[bundle-vite-assets] Bundled ${guideCount} staff guide(s), ${aiCount} AI help file(s), ${mbbsCount} MBBS JSON file(s)`,
);
